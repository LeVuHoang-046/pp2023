class Student:
    def __init__(self,st_id,st_name,st_dob):
        self.st_id = st_id
        self.st_name = st_name
        self.st_dob = st_dob
    
    def getSt_id(self):
        return self.st_id
    
    def getSt_name(self):
        return self.st_name
    
    def getSt_dob(self):
        return self.st_dob